package tictactoe;

public class BoardPoint  {
		private int x;
		private int y;
		
		
          BoardPoint(int x, int y){
        	  this.x=x;
        	  this.y=y;
          }
          
          int getX(){
        	  return x;
          }
          int getY(){
        	  return y;
          }
}
